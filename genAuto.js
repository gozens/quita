/**
 * crée un nouvel element Html
 * @param {Array} className tableau des differentes classes à attribué
 * @param {NameHTMLElement} el le nom de l'element HTML
 * @param {Object} attributs attribut et valeur d'attribut
 * @returns {HTMLElement}
 */
function makeElement(el, className ){
    const element = document.createElement(el)
    className.forEach( (e) =>{
        element.classList.add(e)
    })
    
    return element

}


